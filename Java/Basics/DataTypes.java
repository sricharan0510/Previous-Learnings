class DataTypes {
    public static void main(String[] args) {
        int intVar = 4223;                                 // range : -2^31 to 2^31-1
        short shortVar = 2;                                // range : -2^15 to 2^15-1
        float floatVar = 8.64F;                            // range : 7 bit floating point
        double doubleVar = 8519836.671;                    // range : 15 bit floating point
        char charVar = 'S';                                // range : 
        long longVar = 6300654511L;                        // range : -2^63 to 2^63-1
        byte byteVar = 5;
        boolean boolVar = true;
        System.out.println(intVar);
        System.out.println(floatVar);
        System.out.println(shortVar);
        System.out.println(doubleVar);
        System.out.println(charVar);
        System.out.println(longVar);
        System.out.println(byteVar);
        System.out.println(boolVar);
    }
}